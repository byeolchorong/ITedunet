package chapter03;

public class exam06 {

	public static void main(String[] args) {
		boolean a = true;
		boolean b = false;
		boolean c = !b;			// b의 값을 반대로 바꾸어 대입합니다.
		System.out.println(a);	// true
		System.out.println(!a);	// false // 값을 바꾸어 출력함
		
		System.out.println(b);	// false
		System.out.println(c);	// true 
	}

}
