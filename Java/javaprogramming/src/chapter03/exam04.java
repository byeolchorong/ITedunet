package chapter03;

public class exam04 {

	public static void main(String[] args) 
	{
		
		int a = 10;	// a에 10을 대입
		int b = 3;	// b에 3을 대입
		
		System.out.println(a + b);	// 13
		System.out.println(a - b);	// 7
		System.out.println(a * b);	// 30
		System.out.println(a / b);	// 3
		System.out.println(a % b);	// 1
		
	}

}
